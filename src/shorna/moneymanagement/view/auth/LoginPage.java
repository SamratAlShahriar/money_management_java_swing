    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());

        try {
            UserRepository userRepository = new UserRepository();
            if (email.matches(".*[a-zA-Z0-9].*") || password.length() > 0) {

                boolean isUserExist = userRepository.isUserAlreadyExist(email);

                if (isUserExist) {
                    UserModel result = userRepository.login(email, password);
                    if (result != null) {
                        try {
                            Session session = new Session(result);
                            new DashboardPage().setVisible(true);
                            this.setVisible(false);
                        } catch (Exception e) {
                            e.printStackTrace();
                        }

                    } else {
                        lblMsg.setText("Email and password didn't match");
                    }
                } else {
                    lblMsg.setText("No user with this email");

                }

            } else {
                lblMsg.setText("Insert username and password!");
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

